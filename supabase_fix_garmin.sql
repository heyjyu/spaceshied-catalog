-- 1) 빈 이름 행 삭제 (id 240~243: "갤럭시 워치8 호환" 등 섹션 헤더 잔재) ----
--    혹시 id 가 재배치됐을 수 있어 이름/재질 조건으로도 한 번 더 안전 삭제.
delete from public.products where id in (240, 241, 242, 243);
delete from public.products
  where coalesce(btrim(name), '') = ''
     or name in ('갤럭시 워치8 호환', '갤럭시 워치8 클래식 호환');

-- 2) 가민 스트랩 INSERT (이름 중복이면 건너뜀 → 재실행 안전) --------------
--    model='가민', connector='가민' 이라야 카탈로그 사이드바 "가민" 그룹에 잡힘.
--    재질/체결/규격은 제품명에서 확인 가능한 만큼만 채움(나머지는 관리자에서 보강).
--    D2.3 = '진행'(실판매 누락분) / 포러너 570 = '기획'(미사용 탭).
insert into public.products
  (name, model, connector, base_size, size, material, buckle, status, sort)
select v.name, '가민', '가민', v.base_size, v.size, v.material, v.buckle, v.status, 900
from (values
  -- ── D2.3 바넷봉 호환 (퀵핏 타입) — 진짜 누락분 ───────────────────────
  ('볼트 블랙 버클 실리콘 스트랩',                              '',     '',     '실리콘', '버클',       '진행'),
  ('스탠다드 울트라핏 모노 듀얼 밸크로 패브릭 스트랩',          '',     '',     '패브릭', '밸크로',     '진행'),
  ('퀵핏 스트라이프 마그넷 링크 실리콘 스트랩',                '',     '',     '실리콘', '마그넷 링크', '진행'),
  ('퀵핏 에센셜 솔리드 버클 실리콘 스트랩 26mm',              '26mm', '26mm', '실리콘', '버클',       '진행'),
  ('퀵핏 투톤 메카닉 블랙 버클 실리콘 스트랩',                '',     '',     '실리콘', '버클',       '진행'),
  -- ── 미사용 가민 전용 (포러너 570) — 기획 상태로 보관 ──────────────────
  ('가민 포러너 570 42mm 러너 스피드 실리콘 스트랩',          '20mm', '20mm', '실리콘', '퀵릴리즈',   '기획'),
  ('가민 포러너 570 47mm 러너 스피드 실리콘 스트랩',          '22mm', '22mm', '실리콘', '퀵릴리즈',   '기획')
) as v(name, base_size, size, material, buckle, status)
where not exists (
  select 1 from public.products p where p.name = v.name
);

