--  처리 2가지:
--   1) 빈 이름 행 4개 삭제 — 러그형 탭의 섹션 헤더가 잘못 상품으로 들어옴
--   2) 가민 누락 7종 INSERT — normalize.py SKIP 으로 빠진 가민 스트랩
--      (D2.3 바넷봉 탭은 제품이 '열'로 들어가 파서 실패 / 포러너는 미사용 탭)
--      → DB 가민 0개 였음. 이제 사이드바 "가민" 카테고리에 채워짐.
